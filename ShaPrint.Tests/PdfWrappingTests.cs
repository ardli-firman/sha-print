        [Fact]
        public void Test_PdfWrapping_GeneratedFile_IsValid()
        {
            byte[] minimalJpeg = new byte[]
            {
                0xFF, 0xD8, // SOI
                0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01, 0x01, 0x00, 0x60, 0x00, 0x60, 0x00, 0x00, // JFIF Header
                0xFF, 0xDB, 0x00, 0x43, 0x00, // DQT
                0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08, 0x07, 0x07, 0x07, 0x09, 0x09, 0x08, 0x0A, 0x0C, 0x14,
                0x0D, 0x0C, 0x0B, 0x0B, 0x0C, 0x19, 0x12, 0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D, 0x1A,
                0x1C, 0x1C, 0x20, 0x24, 0x2E, 0x27, 0x20, 0x22, 0x2C, 0x23, 0x1C, 0x1C, 0x28, 0x37, 0x29, 0x2C,
                0x30, 0x31, 0x34, 0x34, 0x34, 0x1F, 0x27, 0x39, 0x3D, 0x38, 0x32, 0x3C, 0x2E, 0x33, 0x34, 0x32,
                0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01, 0x11, 0x00, // SOF0 (1x1 Pixel)
                0xFF, 0xC4, 0x00, 0x14, 0x10, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // DHT
                0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00, 0x37, 0xFF, 0xD9 // SOS + SOI/EOI
            };

            byte[] pdfBytes = ScannerService.WrapJpegInPdf(minimalJpeg);

            Assert.NotNull(pdfBytes);
            Assert.True(pdfBytes.Length > 0);

            // Let's print the hex bytes in the range 241 to 542
            Console.WriteLine("--- Bytes between imageOffset (241) and contentOffset (542) ---");
            for (int i = 241; i < 542 + 10; i++)
            {
                if (i >= pdfBytes.Length) break;
                byte b = pdfBytes[i];
                char c = (b >= 32 && b <= 126) ? (char)b : '.';
                Console.WriteLine($"Offset {i:D3}: Hex={b:X2}, Char={c}");
            }
        }

        [Fact]
        public void Test_MultiPagePdfCompilation_IsValid()
        {
            byte[] minimalJpeg = new byte[]
            {
                0xFF, 0xD8, // SOI
                0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01, 0x01, 0x00, 0x60, 0x00, 0x60, 0x00, 0x00, // JFIF Header
                0xFF, 0xDB, 0x00, 0x43, 0x00, // DQT
                0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08, 0x07, 0x07, 0x07, 0x09, 0x09, 0x08, 0x0A, 0x0C, 0x14,
                0x0D, 0x0C, 0x0B, 0x0B, 0x0C, 0x19, 0x12, 0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D, 0x1A,
                0x1C, 0x1C, 0x20, 0x24, 0x2E, 0x27, 0x20, 0x22, 0x2C, 0x23, 0x1C, 0x1C, 0x28, 0x37, 0x29, 0x2C,
                0x30, 0x31, 0x34, 0x34, 0x34, 0x1F, 0x27, 0x39, 0x3D, 0x38, 0x32, 0x3C, 0x2E, 0x33, 0x34, 0x32,
                0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01, 0x11, 0x00, // SOF0 (1x1 Pixel)
                0xFF, 0xC4, 0x00, 0x14, 0x10, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // DHT
                0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00, 0x37, 0xFF, 0xD9 // SOS + SOI/EOI
            };

            var list = new System.Collections.Generic.List<byte[]> { minimalJpeg, minimalJpeg };
            byte[] pdfBytes = ShaPrint.WpfApp.ViewModels.Pages.ScanViewModel.CompileMultiPagePdf(list);

            Assert.NotNull(pdfBytes);
            Assert.True(pdfBytes.Length > 0);

            // Let's verify standard PDF markers are present
            string pdfText = Encoding.ASCII.GetString(pdfBytes);
            Assert.Contains("%PDF-1.4", pdfText);
            Assert.Contains("/Type /Catalog", pdfText);
            Assert.Contains("/Type /Pages", pdfText);
            Assert.Contains("/Count 2", pdfText);
            Assert.Contains("/Type /Page", pdfText);
            Assert.Contains("startxref", pdfText);
            Assert.Contains("%%EOF", pdfText);
        }
    }
}
